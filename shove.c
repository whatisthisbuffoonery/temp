#include <unistd.h>

void	stats(void)
{
	static int	n;
	char		a;

	a = n + 48;
	n ++;
	write(1, &a, 1);
	write(1, "\n", 1);
}

int	main(void)
{
	int	i;

	i = 0;
	while (i < 5)
	{
		stats();
		i ++;
	}
}
