int	ft_isalnum(int c)
{
	int	a;
	int	b;
	int	num;

	a = (c >= 'a' && c <= 'z');
	b = (c >= 'A' && c <= 'Z');
	num = (c >= '0' && c <= '9');
	return (a || b || num);
}
