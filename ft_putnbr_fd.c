#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int		t;
	char	a;

	if (fd < 0)
		return ;
	if (n < 0)
	{
		write(fd, "-", 1);
		n = 0 - n;
	}
	t = 1;
	while (n / t > 9)
		t *= 10;
	while (t)
	{
		a = ((n / t) % 10) + 48;
		write(fd, &a, 1);
		t /= 10;
	}
}
